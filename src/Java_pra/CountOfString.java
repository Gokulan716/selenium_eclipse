package Java_pra;

public class CountOfString {


	public static void main(String[] args) {
		
     String sr = "Eclipse";
     
     for (int i=0; i<=sr.length()-2; i++) {
    	 
    	 System.out.print(sr.charAt(i)+"="+(i+1)+",");
    
     }
 System.out.print(sr.charAt(sr.length()-1)+"="+sr.length());
 
 //Hash Map
     

	}

}
